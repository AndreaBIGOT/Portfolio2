            <p>En plus de la visite médicale individuelle, la protection de la santé des salariés fait partie de la prévention des risques professionnels.
           <br> L’employeur pourra être tenu responsable en cas d’accident ou de maladie due à des conditions de travail.</p>
            <div style="color: black;font-size: 18px;">
                Ses obligations: 
                <ul>
                    <li>- Il doit empêcher les risques sanitaires et les accidents</li>
                    <li>- Il doit veiller à l’aération des locaux conformément à l’article R 4222-4 du Code du travail</li>
                    <li>- Il doit équiper les locaux d’un matériel de premiers secours (article R 4224-14)</li>
                    <li>- Il doit aussi permettre aux salariés de prendre des repas dans des locaux différents (article R 3511-1 du Code de la santé publique)</li>
